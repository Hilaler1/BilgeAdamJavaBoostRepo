public class ArithmeticOperators {

	public static void main(String[] args) {
		
		int x=3;
		int y=14;
		double sum,sub,multi,mod,div;
		sum=x+y;
		sub=x-y;
		multi= x*y;
		mod=y%x;
		div=y/x;
		
		System.out.println("sum:"+sum+"\n" + "sub:"+sub);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++y);
		System.out.println(y);
	}

}
